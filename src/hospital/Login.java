    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtname.getText();
        String password = txtpass.getText();
        String utype = txtrole.getSelectedItem().toString();
        
        try {
            pst = con.prepareStatement("select * from user where username = ? and passcode = ? and utype = ?");
            
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3,utype);
            
            rs = pst.executeQuery();
            
            if(rs.next())
            {
            int userid = rs.getInt("id");
            this.setVisible(false);
            
            Main obj = new Main(userid,username,utype);
            obj.setVisible(true);
            
            }else
            {
            JOptionPane.showMessageDialog(this, "Username or passsword are incorect");
            txtname.setText("");
            txtpass.setText("");
            txtrole.setSelectedIndex(-1);
            txtname.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

