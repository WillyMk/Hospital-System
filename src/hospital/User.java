    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtname.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String role = txtrole.getSelectedItem().toString();
        
        try {
            pst = con.prepareStatement("Insert into user(name,username,passcode,utype) values (?,?,?,?)");
            pst.setString(1,name);
            pst.setString(2, username);
            pst.setString(3, password);
            pst.setString(4,role);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Username Insterted");
            
            txtname.setText("");
            txtusername.setText("");
            txtpassword.setText("");
            txtrole.setSelectedIndex(-1);
            txtname.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

